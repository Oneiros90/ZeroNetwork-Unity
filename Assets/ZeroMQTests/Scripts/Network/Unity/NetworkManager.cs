using System;
using UnityEngine;

///////////////////////////////////////////////////////////////////////////////
namespace ZeroNetwork.Unity
{
    ///////////////////////////////////////////////////////////////////////////////
    public class NetworkManager : MonoBehaviour
    {

        private readonly Lazy<Network> network = new Lazy<Network>();

        public Network Network => network.Value;

        ///////////////////////////////////////////////////////////////////////////////
        void OnApplicationQuit()
        {
            Network.Stop();
        }
    }
}